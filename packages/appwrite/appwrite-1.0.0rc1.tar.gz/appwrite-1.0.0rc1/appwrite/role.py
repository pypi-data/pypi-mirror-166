class Role:
    @staticmethod
    def any():
        return 'any'

    @staticmethod
    def user(id):
        return f'user:{id}'

    @staticmethod
    def users():
        return 'users'
    
    @staticmethod
    def guests():
        return 'guests'

    @staticmethod
    def team(id, role = ""):
        if role:
            return f'team:{id}/{role}'
        return f'team:{id}'

    @staticmethod
    def status(status):
        return f'status:{status}'