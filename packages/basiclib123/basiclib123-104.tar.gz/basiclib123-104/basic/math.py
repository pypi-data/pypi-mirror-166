import math

def log(a):
    return math.log(a)

def sqrt(a):
    return math.sqrt(a)

def sin(a):
    return math.sin(a)

def cos(a):
    return math.cos(a)

def tan(a):
    return math.tan(a)

def asin(a):
    return math.asin(a)

def acos(a):
    return math.acos(a)

def atan(a):
    return math.atan(a)

